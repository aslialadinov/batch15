package HomeWorkDay2;

public class VariablesValues {
    public static void main(String[] args) {
        /*2) Create a Java program and name it VariableValues
In your program create variables to store all different types of primitive data.
Update(reassign) value of each variable
Print the value of each variable*/
        byte box1= -125;
        box1= 110;
        System.out.println("reassign byte= "+ box1);
        short box2= 3000;
        box2= -2500;
        System.out.println("reassign short="+ box2);
        int box3= 10000;
        box3= -4600;
        System.out.println("reassign int="+ box3);
        long box4= 7868765l;
        box4= -79754437l;
        System.out.println("reassign long="+ box4);
        float box5= 8798.98456f;
        box5= -7465839f;
        System.out.println("reassign float="+ box5);
        double box6= 7394874.38377;
        box6= -787666.87690;
        System.out.println("reassign double="+ box6);
        char box7='l';
        box7= 'l';
        System.out.println("reassign char="+ box7);
        boolean box8= true;
        box8= true;
        System.out.println("reassign boolean="+ box8);


    }
}
